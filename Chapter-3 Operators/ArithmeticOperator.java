public class ArithmeticOperator {
    public static void main(String[] args) {
        float a = 10.5f;  // Declares a float variable 'a' and initializes it to 10.5
        System.out.println(2 + 3);  // Outputs the result of 2 + 3, which is 5
        System.out.println(2 - 3);  // Outputs the result of 2 - 3, which is -1
        System.out.println(20 / 3); // Outputs the result of 20 / 3, which is 6 (integer division)
        System.out.println(20 % 3); // Outputs the remainder of 20 divided by 3, which is 2
        System.out.println(2 * 3);  // Outputs the result of 2 * 3, which is 6
        System.out.println(a++);    // Outputs the current value of 'a' (10.5) and then increments it to 11.5
        System.out.println(++a);    // Increments 'a' first (to 12.5) and then outputs the new value
    }    
}
