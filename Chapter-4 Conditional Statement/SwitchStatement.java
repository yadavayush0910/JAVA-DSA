public class SwitchStatement {
    public static void main(String[] args) {
        int num=12;
        switch(num){
            case 1:
              System.out.println("Num is 1");
              break;
            case 12:
              System.out.println("Num is 12");
              break;
            default:
              System.out.println("Wrong Num");
              break;
        }
    }
}
